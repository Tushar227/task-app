import React from "react";

const Input = ({ item, handleChange, formik, value }) => {
  const { type, placeholder, name, label } = item;
  const error = false;
  return (
    <>
      <label className="text-sm text-gray-900 font-semibold">{label}</label>
      <input
        type={type}
        placeholder={placeholder}
        value={value}
        name={name}
        onChange={handleChange}
        onBlur={formik.handleBlur}
        className={`w-full p-3 outline-none bg-transparent text-xs
        border border-gray-600 rounded-md focus:border-gray-300 ${
          formik.errors[name] ? "border-red-500 focus:border-red-500" : ""
        }`}
      />
      {formik.errors[name] && formik.touched[name] && (
        <span className="text-xs text-red-500">{formik.errors[name]}</span>
      )}
    </>
  );
};

export default Input;

